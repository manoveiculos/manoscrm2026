function parseInitialCsv(): BillingRecord[] {
  const lines = REAL_CSV_DATA.trim().split('\n');
  const records: BillingRecord[] = [];
  
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;
    
    const parts: string[] = [];
    let currentPart = '';
    let inQuotes = false;
    
    for (let charIndex = 0; charIndex < line.length; charIndex++) {
      const char = line[charIndex];
      if (char === '"') {
        inQuotes = !inQuotes;
      } else if (char === ';' && !inQuotes) {
        parts.push(currentPart);
        currentPart = '';
      } else {
        currentPart += char;
      }
    }
    parts.push(currentPart);
    
    if (parts.length < 5) continue;
    
    const rawTelefone = parts[0]?.trim() || '';
    const rawVeiculo = parts[1]?.trim() || '';
    const rawVencimento = parts[2]?.trim() || '';
    const rawValor = parts[3]?.trim() || '0';
    const rawCliente = parts[4]?.trim() || '';
    const rawCpfCnpj = parts[5]?.trim() || '';
    
    const cleanValorStr = rawValor.replace(/\./g, '').replace(',', '.');
    const valor = parseFloat(cleanValorStr) || 0;
    
    let vencimentoFormatted = '';
    const dateParts = rawVencimento.split('/');
    if (dateParts.length === 3) {
      vencimentoFormatted = `${dateParts[2]}-${dateParts[1].padStart(2, '0')}-${dateParts[0].padStart(2, '0')}`;
    } else {
      vencimentoFormatted = rawVencimento;
    }
    
    let status: 'PAGO' | 'PENDENTE' | 'ATRASADO' = 'PENDENTE';
    let dataPagamento: string | undefined = undefined;
    
    if (i % 3 === 0) {
      status = 'PAGO';
      dataPagamento = vencimentoFormatted;
    } else if (vencimentoFormatted < '2026-05-27') {
      status = 'ATRASADO';
    } else {
      status = 'PENDENTE';
    }
    
    records.push({
      id: `pre-${i}`,
      clienteFornecedor: rawCliente || 'Consumidor Final',
      cpfCnpj: rawCpfCnpj || '000.000.000-00',
      telefone: rawTelefone || 'Sem Telefone',
      veiculo: rawVeiculo || 'Nenhum veículo cadastrado',
      vencimento: vencimentoFormatted,
      valor: valor,
      status: status,
      dataPagamento: dataPagamento,
      observacoes: 'Contrato faturado e pré-carregado no sistema corporativo.'
    });
  }
  
  return records;
}

const INITIAL_RECORDS: BillingRecord[] = parseInitialCsv();

export async function fetchBillingRecords(): Promise<BillingRecord[]> {
  try {
    const res = await fetch('/api/billing/records');
    if (res.ok) {
      const records = await res.json();
      
      if (records.length === 0) {
        console.log("Supabase está vazio ou recém-inicializado. Semeando dados...");
        const seedRes = await fetch('/api/billing/import-csv', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ records: INITIAL_RECORDS })
        });
        if (seedRes.ok) {
          const seeded = await fetch('/api/billing/records');
          return seeded.ok ? await seeded.json() : INITIAL_RECORDS;
        }
        return INITIAL_RECORDS;
      }
      return records;
    }
  } catch (err) {
    console.warn("Sem servidor local respondendo à rota /api/billing/records. Fallback ativo.", err);
  }
  
  const data = localStorage.getItem(STORAGE_KEY);
  if (!data) {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(INITIAL_RECORDS));
    return INITIAL_RECORDS;
  }
  try {
    return JSON.parse(data) as BillingRecord[];
  } catch (e) {
    return INITIAL_RECORDS;
  }
}

export async function saveBillingRecord(record: BillingRecord): Promise<BillingRecord> {
  try {
    const res = await fetch('/api/billing/records', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(record)
    });
    if (res.ok) {
      const saved = await res.json();
      if (saved.success) return saved.record;
    }
  } catch (e) {
    console.warn("Falha no servidor ao persistir registro financeiro no Supabase.");
  }

  const records = await fetchBillingRecords();
  const existingIndex = records.findIndex(r => r.id === record.id);
  
  if (existingIndex >= 0) {
    records[existingIndex] = { ...record };
  } else {
    records.unshift(record);
  }
  
  localStorage.setItem(STORAGE_KEY, JSON.stringify(records));
  return record;
}

export async function deleteBillingRecord(id: string): Promise<boolean> {
  try {
    const res = await fetch(`/api/billing/records/${id}`, {
      method: 'DELETE'
    });
    if (res.ok) {
      const data = await res.json();
      if (data.success) return true;
    }
  } catch (e) {
    console.warn("Falha no servidor ao deletar registro no Supabase.");
  }

  const records = await fetchBillingRecords();
  const filtered = records.filter(r => r.id !== id);
  localStorage.setItem(STORAGE_KEY, JSON.stringify(filtered));
  return true;
}

export async function importCsvRecords(newRecords: Omit<BillingRecord, 'id' | 'status'>[]): Promise<BillingRecord[]> {
  const parsedRecords = newRecords.map((rec, idx) => {
    const todayStr = '2026-05-27';
    let status: 'PENDENTE' | 'ATRASADO' = 'PENDENTE';
    
    if (rec.vencimento < todayStr) {
      status = 'ATRASADO';
    }
    
    return {
      ...rec,
      id: `csv-${Date.now()}-${idx}-${crypto.randomUUID().slice(0, 8)}`,
      status,
      observacoes: 'Importado de planilha CSV.'
    };
  });

  try {
    const res = await fetch('/api/billing/import-csv', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ records: parsedRecords })
    });
    if (res.ok) {
      const data = await res.json();
      if (data.success) {
        return fetchBillingRecords();
      }
    }
  } catch (e) {
    console.warn("Falha no servidor ao importar planilha CSV em lote para o Supabase.");
  }

  const existingRecords = await fetchBillingRecords();
  const combined = [...existingRecords];
  
  parsedRecords.forEach(newItem => {
    const isDuplicate = existingRecords.some(exist => 
      exist.cpfCnpj.trim() === newItem.cpfCnpj.trim() && 
      exist.vencimento === newItem.vencimento &&
      exist.veiculo.trim() === newItem.veiculo.trim()
    );
    
    if (!isDuplicate) {
      combined.unshift(newItem as any);
    }
  });
  
  localStorage.setItem(STORAGE_KEY, JSON.stringify(combined));
  return combined;
}

export function calculateStats(records: BillingRecord[]): DashboardStats {
  let totalAReceber = 0;
  let valorRecebido = 0;
  let inadimplencia = 0;
  
  records.forEach(rec => {
    const val = Number(rec.valor) || 0;
    if (rec.status === 'PAGO') {
      valorRecebido += val;
    } else if (rec.status === 'ATRASADO') {
      inadimplencia += val;
    } else {
      totalAReceber += val;
    }
  });
  
  const totalGeral = valorRecebido + totalAReceber + inadimplencia;
  const porcentagemInadimplencia = totalGeral > 0 ? (inadimplencia / totalGeral) * 100 : 0;
  
  return {
    totalAReceber: totalAReceber + inadimplencia,
    valorRecebido,
    inadimplencia,
    porcentagemInadimplencia
  };
}
